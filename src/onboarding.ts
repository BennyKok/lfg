// First-run onboarding state, service-ized like voice-providers.ts: one typed
// JSON file under data/, get/set with patch-merge + validation, wired to
// GET/POST /api/onboarding in serve.ts and folded into /api/bootstrap.
//
// What lives here:
// - profiles: user profiles created through onboarding (the LFG_USERS env
//   roster stays the primary source; these merge into userRoster() so a brand
//   new install with no env config can still create an identity).
// - steps: which onboarding steps are done (profile → agents → first session).
// - completedAt: set once the flow finishes (or is skipped); the frontend
//   gates its full-screen onboarding on this plus "no existing state"
//   (empty roster + empty session list) from the bootstrap payload.
import { readFileSync } from "node:fs";
import { createHash } from "node:crypto";
import { mkdir } from "node:fs/promises";
import { join } from "node:path";
import { PATHS } from "./config.ts";

export type OnboardingProfile = {
  email: string;
  name: string;
  createdAt: string;
  // File name under data/avatars/ when the user uploaded a photo during
  // onboarding; userRoster() serves it via /api/avatars/<file> instead of
  // falling back to Gravatar.
  avatar?: string;
};

export type OnboardingSteps = {
  profile: boolean;
  agents: boolean;
  repo: boolean;
  firstSession: boolean;
};

export type OnboardingState = {
  profiles: OnboardingProfile[];
  steps: OnboardingSteps;
  completedAt: string | null;
};

const DEFAULT_STEPS: OnboardingSteps = {
  profile: false,
  agents: false,
  repo: false,
  firstSession: false,
};

const DEFAULTS: OnboardingState = {
  profiles: [],
  steps: { ...DEFAULT_STEPS },
  completedAt: null,
};

const filePath = () => join(PATHS.data, "onboarding.json");

const EMAIL_RE = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;

function sanitize(raw: unknown): OnboardingState {
  if (!raw || typeof raw !== "object") return structuredClone(DEFAULTS);
  const p = raw as Partial<OnboardingState>;
  const profiles = Array.isArray(p.profiles)
    ? p.profiles
        .filter(
          (x): x is OnboardingProfile =>
            !!x && typeof x === "object" && typeof (x as OnboardingProfile).email === "string",
        )
        .filter((x) => EMAIL_RE.test(x.email))
        .map((x) => ({
          email: x.email.trim().toLowerCase(),
          name: typeof x.name === "string" ? x.name.trim() : "",
          createdAt: typeof x.createdAt === "string" ? x.createdAt : new Date(0).toISOString(),
          // Avatar file names are generated by setProfileAvatar; reject
          // anything that could path-traverse out of data/avatars.
          ...(typeof x.avatar === "string" && /^[a-z0-9._-]+$/i.test(x.avatar)
            ? { avatar: x.avatar }
            : {}),
        }))
    : [];
  const steps = { ...DEFAULT_STEPS };
  if (p.steps && typeof p.steps === "object") {
    for (const key of Object.keys(DEFAULT_STEPS) as (keyof OnboardingSteps)[]) {
      if (typeof p.steps[key] === "boolean") steps[key] = p.steps[key];
    }
  }
  return {
    profiles,
    steps,
    completedAt: typeof p.completedAt === "string" ? p.completedAt : null,
  };
}

export async function getOnboarding(): Promise<OnboardingState> {
  const f = Bun.file(filePath());
  if (!(await f.exists())) return structuredClone(DEFAULTS);
  try {
    return sanitize(JSON.parse(await f.text()));
  } catch {
    return structuredClone(DEFAULTS);
  }
}

// Sync read for users.ts, whose roster helpers (USERS-adjacent) are called
// from synchronous paths and can't await. Same validation/fallback.
export function onboardingProfilesSync(): OnboardingProfile[] {
  try {
    return sanitize(JSON.parse(readFileSync(filePath(), "utf8"))).profiles;
  } catch {
    return [];
  }
}

async function write(state: OnboardingState): Promise<OnboardingState> {
  await mkdir(PATHS.data, { recursive: true });
  await Bun.write(filePath(), JSON.stringify(state, null, 2));
  return state;
}

// Patch-merge steps/completion. `completed: true` stamps completedAt (idempotent),
// `completed: false` clears it (lets a dev re-run the flow).
export async function patchOnboarding(patch: {
  steps?: Partial<OnboardingSteps>;
  completed?: boolean;
}): Promise<OnboardingState> {
  const cur = await getOnboarding();
  const steps = { ...cur.steps };
  if (patch.steps && typeof patch.steps === "object") {
    for (const key of Object.keys(DEFAULT_STEPS) as (keyof OnboardingSteps)[]) {
      if (typeof patch.steps[key] === "boolean") steps[key] = patch.steps[key]!;
    }
  }
  let completedAt = cur.completedAt;
  if (patch.completed === true) completedAt = completedAt ?? new Date().toISOString();
  else if (patch.completed === false) completedAt = null;
  return write({ ...cur, steps, completedAt });
}

// Create (or update the name of) an onboarding profile. Marks the profile
// step done as a side effect — creating a profile IS completing that step.
// Throws on invalid email so the route can 400 with the message.
export async function addOnboardingProfile(input: {
  email?: unknown;
  name?: unknown;
}): Promise<OnboardingState> {
  const email = typeof input.email === "string" ? input.email.trim().toLowerCase() : "";
  const name = typeof input.name === "string" ? input.name.trim() : "";
  if (!EMAIL_RE.test(email)) throw new Error("expected a valid email");
  const cur = await getOnboarding();
  const existing = cur.profiles.find((p) => p.email === email);
  const profiles = existing
    ? cur.profiles.map((p) => (p.email === email ? { ...p, name: name || p.name } : p))
    : [...cur.profiles, { email, name, createdAt: new Date().toISOString() }];
  return write({ ...cur, profiles, steps: { ...cur.steps, profile: true } });
}

// ------------------------------------------------------------ avatars

export const AVATARS_DIR = () => join(PATHS.data, "avatars");

const AVATAR_EXT: Record<string, string> = {
  "image/png": "png",
  "image/jpeg": "jpg",
  "image/webp": "webp",
  "image/gif": "gif",
};

export const AVATAR_MIME_BY_EXT: Record<string, string> = {
  png: "image/png",
  jpg: "image/jpeg",
  webp: "image/webp",
  gif: "image/gif",
};

const AVATAR_MAX_BYTES = 5 * 1024 * 1024;

// Store an uploaded profile photo for an onboarding profile. File name is
// derived from the email hash (stable, no user input in the path) + extension
// from the validated mime type. Throws with a user-facing message on bad
// input so the route can 400.
export async function setProfileAvatar(
  email: string,
  bytes: Uint8Array,
  mimeType: string,
): Promise<OnboardingState> {
  const ext = AVATAR_EXT[mimeType];
  if (!ext) throw new Error("expected a png, jpeg, webp, or gif image");
  if (!bytes.length) throw new Error("empty image");
  if (bytes.length > AVATAR_MAX_BYTES) throw new Error("image too large (max 5MB)");
  const normalized = email.trim().toLowerCase();
  const cur = await getOnboarding();
  if (!cur.profiles.some((p) => p.email === normalized)) {
    throw new Error("no profile for that email — create the profile first");
  }
  const file = `${createHash("md5").update(normalized).digest("hex")}.${ext}`;
  await mkdir(AVATARS_DIR(), { recursive: true });
  await Bun.write(join(AVATARS_DIR(), file), bytes);
  const profiles = cur.profiles.map((p) =>
    p.email === normalized ? { ...p, avatar: file } : p,
  );
  return write({ ...cur, profiles });
}
